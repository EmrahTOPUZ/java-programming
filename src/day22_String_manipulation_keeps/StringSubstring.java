package day22_String_manipulation_keeps;

public class StringSubstring {
    public static void main(String[] args) {

        String word = "Cimbom is champion";

        System.out.println(word.substring(0,6));
        System.out.println(word.substring(0,9));
        System.out.println(word.substring(7,9));
        System.out.println(word.substring(10,18));
        System.out.println(word.substring(0));




    }
}
